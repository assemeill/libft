#include "libft.h"

char	*ft_strcat(char *dst, char *src)
{
	int i;

	i = 0;
	while (dst[i] != '\0')
		i++;
	if (dst[i] == '\0')
	{
		while (*src != '\0')
			dst[i++] = *src++;
		dst[i] = '\0';
	}
	return (dst);
}
