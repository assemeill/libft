#include "libft.h"

int		ft_words_count(char const *s, char c)
{
	int n;

	n = 0;
	while (*s)
	{
		while (*s == c)
			s++;
		if (*s != c && *s != '\0')
			n++;
		while (*s != c && *s != '\0')
			s++;
	}
	return (n);
}
