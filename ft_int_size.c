#include "libft.h"

int		ft_int_size(int n)
{
	int i;

	if (n == 0)
		i = 1;
	if (ft_if_negative(n))
		i = 1;
	else
		i = 0;
	while (n / 10 != 0)
	{
		n /= 10;
		i++;
	}
	return (i);
}
