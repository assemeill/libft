#include "libft.h"

char	*ft_strncat(char *dst, char *src, size_t n)
{
	size_t i;

	i = 0;
	while (dst[i] != '\0')
		i++;
	if (dst[i] == '\0')
	{
		while (*src != '\0' && n-- > 0)
			dst[i++] = *src++;
		dst[i] = '\0';
	}
	return (dst);
}
